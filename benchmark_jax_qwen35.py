from __future__ import annotations

import argparse
import json
import math
import time
from pathlib import Path
from typing import Any

import jax
import jax.numpy as jnp

from turboquant_jax import TurboQuantCompressorMSEJAX, TurboQuantCompressorV2JAX


def _block_tree(value: Any) -> None:
    leaves = jax.tree_util.tree_leaves(value)
    for leaf in leaves:
        if hasattr(leaf, "block_until_ready"):
            leaf.block_until_ready()


def _time_call(fn, *args, repeats: int = 6, warmup: int = 2):
    result = None
    for _ in range(warmup):
        result = fn(*args)
        _block_tree(result)

    timings = []
    for _ in range(repeats):
        start = time.perf_counter()
        result = fn(*args)
        _block_tree(result)
        timings.append((time.perf_counter() - start) * 1000.0)

    return sum(timings) / len(timings), result


def _parse_qwen35_config(model_dir: Path) -> dict[str, int]:
    cfg = json.loads((model_dir / "config.json").read_text(encoding="utf-8"))
    text_cfg = cfg.get("text_config", {}) if isinstance(cfg.get("text_config", {}), dict) else {}
    merged = {**cfg, **text_cfg}

    num_layers = int(merged.get("num_hidden_layers", merged.get("n_layer", 0)))
    num_attn_heads = int(merged.get("num_attention_heads", merged.get("n_head", 0)))
    num_kv_heads = int(
        merged.get(
            "linear_num_key_heads",
            merged.get("num_key_value_heads", merged.get("num_attention_heads", 0)),
        )
    )
    hidden_size = int(merged.get("hidden_size", merged.get("n_embd", 0)))
    head_dim = int(
        merged.get(
            "linear_key_head_dim",
            merged.get("head_dim", hidden_size // max(num_attn_heads, 1)),
        )
    )
    return {
        "num_layers": num_layers,
        "num_attention_heads": num_attn_heads,
        "num_key_value_heads": num_kv_heads,
        "hidden_size": hidden_size,
        "head_dim": head_dim,
    }


def _synth_kv(key: jax.Array, b: int, h: int, s: int, d: int) -> tuple[jnp.ndarray, jnp.ndarray, jnp.ndarray]:
    k1, k2 = jax.random.split(key)
    keys = jax.random.normal(k1, (b, h, s, d), dtype=jnp.float16)
    values = jax.random.normal(k2, (b, h, s, d), dtype=jnp.float16)

    keys = keys / (jnp.linalg.norm(keys, axis=-1, keepdims=True) + 1e-8)
    values = values / (jnp.linalg.norm(values, axis=-1, keepdims=True) + 1e-8)
    queries = keys[:, :, -1:, :]
    return keys, values, queries


def _tensor_bytes(value: Any) -> int:
    if isinstance(value, dict):
        return sum(_tensor_bytes(v) for v in value.values())
    if isinstance(value, (list, tuple)):
        return sum(_tensor_bytes(v) for v in value)
    arr = jnp.asarray(value)
    return int(arr.size * arr.dtype.itemsize)


def _append_report_section(report_path: Path, section: str, marker: str) -> None:
    text = report_path.read_text(encoding="utf-8") if report_path.exists() else ""
    if marker in text:
        before, _, _ = text.partition(marker)
        text = before.rstrip() + "\n\n" + section
    else:
        text = text.rstrip() + "\n\n" + section if text.strip() else section
    report_path.write_text(text, encoding="utf-8")


def _render_section(rows: list[dict[str, Any]], device: str, seq_len: int, bits_list: list[int], score_cache_policy: str, tile_size: int) -> str:
    marker = "## TurboQuant JAX Qwen3.5 Comparison"
    lines: list[str] = [marker, ""]
    lines.append(f"- Device: {device}")
    lines.append(f"- Synthetic KV sequence length: {seq_len}")
    lines.append(f"- Score cache policy: {score_cache_policy}")
    lines.append(f"- Tiled scoring tile size: {tile_size}")
    lines.append(f"- Fused score backend: {rows[0]['score_backend'] if rows else 'xla'}")
    lines.append("- Methods compared: baseline fp16 attention, MSE-only packed JAX compression, and TurboQuant packed JAX compression")
    lines.append("- This section is auto-generated by benchmark_jax_qwen35.py.")
    lines.append("")
    lines.append("| Model | Method | Bits | Head dim | KV heads | Layers | Baseline score (ms) | Key compress (ms) | Value compress (ms) | Compressed score (ms) | Raw KV (MB) | Stored KV (MB) | Runtime KV (MB) | Compression | Runtime compression |")
    lines.append("| --- | --- | ---: | ---: | ---: | ---: | ---: | ---: | ---: | ---: | ---: | ---: | ---: | ---: | ---: |")

    for row in rows:
        lines.append(
            "| {model} | {method} | {bits} | {head_dim} | {kv_heads} | {layers} | {baseline_ms:.2f} | {key_ms:.2f} | {val_ms:.2f} | {score_ms:.2f} | {raw_mb:.2f} | {stored_mb:.2f} | {runtime_mb:.2f} | {ratio:.2f}x | {runtime_ratio:.2f}x |".format(
                model=row["model"],
                method=row["method"],
                bits=row["bits"],
                head_dim=row["head_dim"],
                kv_heads=row["kv_heads"],
                layers=row["layers"],
                baseline_ms=row["baseline_score_ms"],
                key_ms=row["key_compress_ms"],
                val_ms=row["value_compress_ms"],
                score_ms=row["compressed_score_ms"],
                raw_mb=row["raw_kv_mb"],
                stored_mb=row["stored_kv_mb"],
                runtime_mb=row["runtime_kv_mb"],
                ratio=row["compression_ratio"],
                runtime_ratio=row["runtime_compression_ratio"],
            )
        )

    lines.append("")
    lines.append("### Method Summary")
    lines.append("")
    lines.append("| Method | Avg baseline score (ms) | Avg compressed score (ms) | Avg compression |")
    lines.append("| --- | ---: | ---: | ---: |")

    grouped: dict[str, list[dict[str, Any]]] = {}
    for row in rows:
        grouped.setdefault(row["method"], []).append(row)

    for method in sorted(grouped):
        items = grouped[method]
        avg_baseline = sum(item["baseline_score_ms"] for item in items) / len(items)
        avg_compressed = sum(item["compressed_score_ms"] for item in items) / len(items)
        avg_ratio = sum(item["compression_ratio"] for item in items) / len(items)
        lines.append(f"| {method} | {avg_baseline:.2f} | {avg_compressed:.2f} | {avg_ratio:.2f}x |")

    lines.append("")
    return "\n".join(lines)


def main() -> None:
    parser = argparse.ArgumentParser(description="Benchmark TurboQuant JAX packed compressor on local Qwen3.5 configs")
    parser.add_argument("--base-model-root", default="/mnt/c/models/base", help="Path containing Qwen3.5 base model folders")
    parser.add_argument("--report-path", default="/mnt/c/Users/zshua/Downloads/TQ-Experimentation/benchmark_qwen35_turboquant_rotorquant.md", help="Markdown report file to append/replace section")
    parser.add_argument("--device", choices=["cpu", "gpu"], default="gpu", help="JAX device type")
    parser.add_argument("--bits", nargs="*", type=int, default=[2, 3, 4], help="Bit widths to benchmark")
    parser.add_argument("--seq-len", type=int, default=2048, help="Synthetic KV sequence length")
    parser.add_argument("--repeats", type=int, default=6, help="Timed repeats")
    parser.add_argument("--warmup", type=int, default=2, help="Warmup rounds")
    parser.add_argument(
        "--score-cache-policy",
        choices=["prepared", "packed"],
        default="prepared",
        help="prepared keeps unpacked tensors for faster repeated scoring; packed minimizes memory",
    )
    parser.add_argument(
        "--score-backend",
        choices=["xla", "pallas"],
        default="xla",
        help="Backend for fused dequant plus attention term1 kernel",
    )
    parser.add_argument("--tile-size", type=int, default=256, help="Tile size for tiled scoring kernel")
    args = parser.parse_args()

    model_root = Path(args.base_model_root)
    report_path = Path(args.report_path)

    devices = jax.devices(args.device)
    if not devices:
        raise RuntimeError(f"No JAX devices found for {args.device}")
    device = devices[0]

    model_dirs = sorted([p for p in model_root.iterdir() if p.is_dir() and p.name.startswith("Qwen3.5-") and p.name.endswith("-Base")])
    if not model_dirs:
        raise FileNotFoundError(f"No Qwen3.5 base model folders found under {model_root}")

    rows: list[dict[str, Any]] = []
    key = jax.random.PRNGKey(0)

    for model_dir in model_dirs:
        cfg = _parse_qwen35_config(model_dir)
        b = 1
        h = max(1, cfg["num_key_value_heads"])
        d = max(1, cfg["head_dim"])
        s = args.seq_len

        key, subkey = jax.random.split(key)
        keys, values, queries = _synth_kv(subkey, b, h, s, d)
        keys = jax.device_put(keys, device)
        values = jax.device_put(values, device)
        queries = jax.device_put(queries, device)

        baseline_fn = jax.jit(lambda q, k: jnp.matmul(q, jnp.swapaxes(k, -2, -1)))
        baseline_ms, _ = _time_call(baseline_fn, queries, keys, repeats=args.repeats, warmup=args.warmup)

        raw_kv_bytes = int(keys.size * keys.dtype.itemsize + values.size * values.dtype.itemsize)

        rows.append(
            {
                "model": model_dir.name,
                "method": "baseline-fp16",
                "bits": 16,
                "head_dim": d,
                "kv_heads": h,
                "layers": cfg["num_layers"],
                "baseline_score_ms": float(baseline_ms),
                "key_compress_ms": 0.0,
                "value_compress_ms": 0.0,
                "compressed_score_ms": float(baseline_ms),
                "raw_kv_mb": float(raw_kv_bytes / (1024 * 1024)),
                "stored_kv_mb": float(raw_kv_bytes / (1024 * 1024)),
                "runtime_kv_mb": float(raw_kv_bytes / (1024 * 1024)),
                "compression_ratio": 1.0,
                "runtime_compression_ratio": 1.0,
                "score_backend": "xla",
            }
        )

        for bits in args.bits:
            method = f"turboquant-jax-packed-{bits}bit"
            key_comp = TurboQuantCompressorV2JAX(
                head_dim=d,
                bits=bits,
                seed=42,
                tile_size=args.tile_size,
                score_backend=args.score_backend,
            )
            val_comp = TurboQuantCompressorMSEJAX(head_dim=d, bits=bits, seed=77)

            key_ms, compressed_k = _time_call(key_comp.compress, keys, repeats=args.repeats, warmup=args.warmup)
            val_ms, compressed_v = _time_call(val_comp.compress, values, repeats=args.repeats, warmup=args.warmup)

            prep_ms = 0.0
            prepared_k = None
            if args.score_cache_policy == "prepared":
                prep_ms, prepared_k = _time_call(
                    key_comp.prepare_for_scoring,
                    compressed_k,
                    repeats=max(1, args.repeats // 2),
                    warmup=1,
                )
                score_ms, _ = _time_call(
                    key_comp.asymmetric_attention_scores_prepared,
                    queries,
                    prepared_k,
                    repeats=max(2, args.repeats // 2),
                    warmup=args.warmup,
                )
            else:
                score_ms, _ = _time_call(
                    key_comp.asymmetric_attention_scores,
                    queries,
                    compressed_k,
                    repeats=max(2, args.repeats // 2),
                    warmup=args.warmup,
                )

            stored_bytes = _tensor_bytes(compressed_k) + _tensor_bytes(compressed_v)
            prepared_bytes = _tensor_bytes(prepared_k) if prepared_k is not None else 0
            runtime_bytes = stored_bytes + prepared_bytes
            rows.append(
                {
                    "model": model_dir.name,
                    "method": method,
                    "bits": bits,
                    "head_dim": d,
                    "kv_heads": h,
                    "layers": cfg["num_layers"],
                    "baseline_score_ms": float(baseline_ms),
                    "key_compress_ms": float(key_ms + prep_ms),
                    "value_compress_ms": float(val_ms),
                    "compressed_score_ms": float(score_ms),
                    "raw_kv_mb": float(raw_kv_bytes / (1024 * 1024)),
                    "stored_kv_mb": float(stored_bytes / (1024 * 1024)),
                    "runtime_kv_mb": float(runtime_bytes / (1024 * 1024)),
                    "compression_ratio": float(raw_kv_bytes / stored_bytes) if stored_bytes > 0 else math.inf,
                    "runtime_compression_ratio": float(raw_kv_bytes / runtime_bytes) if runtime_bytes > 0 else math.inf,
                    "score_backend": key_comp.score_backend,
                }
            )

            print(
                f"{model_dir.name} {method}: baseline={baseline_ms:.2f}ms, key={key_ms:.2f}ms, prep={prep_ms:.2f}ms, "
                f"value={val_ms:.2f}ms, score={score_ms:.2f}ms, ratio={raw_kv_bytes / stored_bytes:.2f}x, "
                f"runtime_ratio={raw_kv_bytes / runtime_bytes:.2f}x",
                flush=True,
            )

            key_mse = TurboQuantCompressorMSEJAX(head_dim=d, bits=bits, seed=99)
            mse_key_ms, mse_compressed_k = _time_call(key_mse.compress, keys, repeats=args.repeats, warmup=args.warmup)
            mse_val_ms, mse_compressed_v = _time_call(val_comp.compress, values, repeats=args.repeats, warmup=args.warmup)

            def _mse_score(q: jnp.ndarray, ck: dict[str, Any]) -> jnp.ndarray:
                recon = key_mse.decompress(ck)
                return jnp.matmul(q.astype(jnp.float32), jnp.swapaxes(recon.astype(jnp.float32), -2, -1))

            mse_score_ms, _ = _time_call(
                _mse_score,
                queries,
                mse_compressed_k,
                repeats=max(2, args.repeats // 2),
                warmup=args.warmup,
            )

            mse_stored_bytes = _tensor_bytes(mse_compressed_k) + _tensor_bytes(mse_compressed_v)
            rows.append(
                {
                    "model": model_dir.name,
                    "method": f"mse-only-jax-packed-{bits}bit",
                    "bits": bits,
                    "head_dim": d,
                    "kv_heads": h,
                    "layers": cfg["num_layers"],
                    "baseline_score_ms": float(baseline_ms),
                    "key_compress_ms": float(mse_key_ms),
                    "value_compress_ms": float(mse_val_ms),
                    "compressed_score_ms": float(mse_score_ms),
                    "raw_kv_mb": float(raw_kv_bytes / (1024 * 1024)),
                    "stored_kv_mb": float(mse_stored_bytes / (1024 * 1024)),
                    "runtime_kv_mb": float(mse_stored_bytes / (1024 * 1024)),
                    "compression_ratio": float(raw_kv_bytes / mse_stored_bytes) if mse_stored_bytes > 0 else math.inf,
                    "runtime_compression_ratio": float(raw_kv_bytes / mse_stored_bytes) if mse_stored_bytes > 0 else math.inf,
                    "score_backend": "xla",
                }
            )

            print(
                f"{model_dir.name} mse-only-jax-packed-{bits}bit: key={mse_key_ms:.2f}ms, "
                f"value={mse_val_ms:.2f}ms, score={mse_score_ms:.2f}ms, ratio={raw_kv_bytes / mse_stored_bytes:.2f}x",
                flush=True,
            )

    section = _render_section(rows, str(device), args.seq_len, args.bits, args.score_cache_policy, args.tile_size)
    _append_report_section(report_path, section, "## TurboQuant JAX Qwen3.5 Comparison")
    print(f"Updated report at {report_path}")


if __name__ == "__main__":
    main()
